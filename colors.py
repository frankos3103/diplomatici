def red(string):
    return f"\033[31m{string}\033[0m"

def green(string):
    return f"\033[32m{string}\033[0m"

def yellow(string):
    return f"\033[33m{string}\033[0m"

def blue(string):
    return f"\033[34m{string}\033[0m"

def purple(string):
    return f"\033[35m{string}\033[0m"

def cyan(string):
    return f"\033[36m{string}\033[0m"

def grey(string):
    return f"\033[90m{string}\033[0m"